    private void btnproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproductoActionPerformed

        Connection conn = null;
        String RUTALOCAL = System.getProperty("user.dir");
        try {
            conn = conexion.conexion.Enlace(conn);
            HashMap parametros = new HashMap();
            parametros.put("usuario", txtnombreusuario.getText());
            JasperPrint jp = JasperFillManager.fillReport(RUTALOCAL + "/src/informes/producto.jasper", parametros, conn);
            JasperViewer view = new JasperViewer(jp, false);
            view.setTitle("PROUCTO");
            view.setExtendedState(Frame.MAXIMIZED_BOTH);

            view.setVisible(true);
            conn.close();

        } catch (SQLException ex) {
            Logger.getLogger(menuprincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            Logger.getLogger(menuprincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnproductoActionPerformed

