    public void Fecha() {
        String sql = "select curdate() as fecha";
        try {

            st = conexion.conexion.sta(st);
            rs = st.executeQuery(sql);
            rs.next();

            txtfecha.setText(rs.getString("fecha"));

            DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date date = new Date();
            String fecha = formato.format(date);
            st.close();
            rs.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No conecta");
            //JOptionPane.showMessageDialog(null, "Error" + e, "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }

    }

    class escucha implements ActionListener {

        public void actionPerformed(ActionEvent e) {
            Calendar ahora = Calendar.getInstance();
            int hor = ahora.get(Calendar.HOUR_OF_DAY);
            int min = ahora.get(Calendar.MINUTE);
            int seg = ahora.get(Calendar.SECOND);
            txthora.setText(hor + ":" + min + ":" + seg);
        }
    }
